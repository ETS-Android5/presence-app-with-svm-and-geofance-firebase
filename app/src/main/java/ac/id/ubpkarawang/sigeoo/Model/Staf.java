package ac.id.ubpkarawang.sigeoo.Model;

public class Staf {
    private String id_staf;
    private String nama_staf;
    private String email_staf;
    private String alamat_staf;
    private String foto_staf;
    private String date_created;
    private String date_updated;

    public String getId_staf() {
        return id_staf;
    }

    public void setId_staf(String id_staf) {
        this.id_staf = id_staf;
    }

    public String getNama_staf() {
        return nama_staf;
    }

    public void setNama_staf(String nama_staf) {
        this.nama_staf = nama_staf;
    }

    public String getEmail_staf() {
        return email_staf;
    }

    public void setEmail_staf(String email_staf) {
        this.email_staf = email_staf;
    }

    public String getAlamat_staf() {
        return alamat_staf;
    }

    public void setAlamat_staf(String alamat_staf) {
        this.alamat_staf = alamat_staf;
    }

    public String getFoto_staf() {
        return foto_staf;
    }

    public void setFoto_staf(String foto_staf) {
        this.foto_staf = foto_staf;
    }

    public String getDate_created() {
        return date_created;
    }

    public void setDate_created(String date_created) {
        this.date_created = date_created;
    }

    public String getDate_updated() {
        return date_updated;
    }

    public void setDate_updated(String date_updated) {
        this.date_updated = date_updated;
    }
}
